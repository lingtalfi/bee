namespace Bee\Abstractive\EventDispatcher;


/**
 * Event
 * @author Lingtalfi
 * 2015-02-15
 *
 */
class Event
{
    private $propagationStopped = false;


    public function isPropagationStopped()
    {
        return $this->propagationStopped;
    }

    public function stopPropagation()
    {
        $this->propagationStopped = true;
    }

}
